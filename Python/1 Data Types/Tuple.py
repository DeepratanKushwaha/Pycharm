""""Tuple and list both are same the only difference is that in the tuples
    values are closed with () and tuples are immutable(not changeable)
"""

marks = (99, 78, 87, 35, 65, 76, 32, 24, 100)
print(marks)
print("Marks:", marks)
print(marks[3])
print(marks[1:4])
print(marks[3:])
print(marks[::])
print(marks[::-1])
print(marks[::-2])
print(marks+marks)
print(marks*3)
